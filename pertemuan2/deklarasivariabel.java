public class deklarasivariabel {
    public static void main(String[] args) {
        // Deklarasi variabel
        int angka = 10;                 // Tipe data integer
        double desimal = 3.14;          // Tipe data double (pecahan)
        char huruf = 'A';               // Tipe data karakter
        String teks = "Belajar Java";   // Tipe data string (teks)
        boolean status = true;          // Tipe data boolean (true/false)

        // Menampilkan nilai variabel
        System.out.println("Nilai integer: " + angka);
        System.out.println("Nilai double: " + desimal);
        System.out.println("Nilai char: " + huruf);
        System.out.println("Nilai string: " + teks);
        System.out.println("Nilai boolean: " + status);
    }
}
