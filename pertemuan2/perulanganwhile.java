public class perulanganwhile{
    public static void main(String[] args) {
        int i = 1; // Inisialisasi variabel i

        // Perulangan while untuk mencetak angka 1 hingga 10
        while (i <= 10) {
            System.out.println("Angka: " + i);
            i++; // Increment i setiap iterasi
        }
    }
}
